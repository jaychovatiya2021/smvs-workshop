package org.example;

public class DemoDatatype {
    public static void main(String[] args) {

        byte theByte = -128;
        short theShort = 32_000;
        int theInt = 787_838_334;
        long theLong = 787_838_334_432L;

        float pi = 600.650F;
        double doublePi = 3.1415;
        boolean isAdult  = true;
        char nameInitial = 'A';


        System.out.println("byte = " +theByte);
        System.out.println("Short = " +theShort);
        System.out.println("int = " +theInt);
        System.out.println("long = " +theLong);
        System.out.println("float = " +pi);
        System.out.println("double = " + doublePi);
        System.out.println("boolean = " + isAdult);
        System.out.println("char = " + nameInitial);

    }
}
